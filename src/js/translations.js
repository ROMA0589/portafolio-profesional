let currentLanguage = 'es';

function initializeLanguage() {
    const savedLanguage = localStorage.getItem('preferred-language') || 'es';
    changeLanguage(savedLanguage);
    
    const languageSelect = document.getElementById('language-select');
    languageSelect.value = savedLanguage;
    languageSelect.addEventListener('change', (e) => {
        changeLanguage(e.target.value);
    });
}

function changeLanguage(language) {
    if (!translations[language]) return;
    
    currentLanguage = language;
    localStorage.setItem('preferred-language', language);
    document.documentElement.lang = language;
    
    // Actualizar todos los elementos con data-translate
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (translations[language][key]) {
            element.textContent = translations[language][key];
        }
    });
    
    // Actualizar placeholders
    document.querySelectorAll('[data-placeholder]').forEach(element => {
        const key = element.getAttribute('data-placeholder');
        if (translations[language][key]) {
            element.placeholder = translations[language][key];
        }
    });
    
    // Actualizar el tÃ­tulo de la pÃ¡gina
    const titleElement = document.querySelector('title');
    if (titleElement && translations[language]['page.title']) {
        titleElement.textContent = translations[language]['page.title'];
    }
}

